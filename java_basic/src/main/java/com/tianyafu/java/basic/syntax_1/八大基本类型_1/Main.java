package com.tianyafu.java.basic.syntax_1.八大基本类型_1;

public class Main {

    public static void main(String[] args) {
        /**
         * 八大基本类型：
         * byte short int long float double boolean char
         */
        byte a  = 1;  // 1字节
        short b = 2;  // 2字节
        int c = 1;    // 4字节
        long d = 1L;  // 8字节
        float e = 2.1f; // 4字节
        double f = 2.1; // 8字节
        boolean g = true; // 1字节
        char h = 97; // 2字节
    }
}
